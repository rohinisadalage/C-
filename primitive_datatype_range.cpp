#include <iostream>
using namespace std;
int main ()
{
    cout << "size of int data type:\t" << sizeof (int) << endl;
    cout << "size of long data type:\t" << sizeof (long) << endl;
    cout << "size of char data type:\t" << sizeof (char) << endl;
    cout << "size of bool data type:\t" << sizeof (bool) << endl;
    cout << "size of float data type:\t" << sizeof (float) << endl;
    cout << "size of double data type:\t" << sizeof (double) << endl;
    cout << "size of short data type:\t" << sizeof (short) << endl;
    return 0;
}